// %d is format specifer for integer

// Type of Instruction

// Type Declaration Instruction

# include <stdio.h>
int main(void) {
    int a = 1, b = 2, c = 3, d = 4, e = 5,f = 6;
    printf("The value of a %d is and the value of b is %d\n",a,b);
    printf("The value of c %d is and the value of d is %d\n",c,d);
    printf("The value of e %d is and the value of f is %d\n",e,f);
    return 0;

}
